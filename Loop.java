public class Loop {
    public static void main(String[] args) {
        for (int i = 0; // membuat variable int dgn value 0
                i <= 4; // menjalankan perintah di {} jika kondisi benar
                i++ // menambahkan nilai variable i setelah selesai menjalankan code
                ) {
            System.out.println("Helo!");
        }
    }
}
