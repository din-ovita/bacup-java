public class JavaVariable {
    public static void main(String[] args) {
        System.out.println("Materi Java Variable");
        String nama = "Dinda", lengkap = " Novita";
        int x = 9, y = 8, z = 7;
        System.out.println(nama + lengkap);
        System.out.println(x + y + z);
        System.out.println((x * 100) + (y * 10) + (7));
        System.out.println("" + x + y + z);
    }
}